let C = prompt("Konversi Suhu Celcius ke Reamur");
console.log("Suhu Dalam Celcius: " + C);

let C2 = prompt("Konversi Suhu Celcius ke Fahrenheit");
console.log("Suhu Dalam Celcius: " + C2);

const CR = (C / 5) * 4;
const CF = (C2 * 9) / 5 + 32;
console.log("Hasil Konversi Celcius ke Reamur: " + CR);
console.log("Hasil Konversi Celcius ke Fahrenheit: " + CF);
