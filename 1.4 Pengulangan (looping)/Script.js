// For...

for (let i = 1; i <= 10; i++) {
  console.log("Hello World" + i);
}

// While..

let i = 1;
while (i <= 10) {
  console.log("Hello World" + i);
  i++;
}

let i1 = 10;
while (i <= 1) {
  console.log("Hello World" + i);
  i1++;
}

// Do while

let i_ = 0

do {
    console.log("Hello World" + i_)
    i_++
}
while (i_ <= 10)