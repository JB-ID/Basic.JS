// Volume Balok
let p = 10;
let l = 8;
let t = 4;

const Volume_balok = p * l * t;
console.log("Volume Balok", Volume_balok);

// Volume Kubus
let s1 = 5;
let s2 = 5;
let s3 = 5;

const Volume_Kubus = s1 * s2 * s3;
console.log("Volume Kubus", Volume_Kubus);

// Volume Tabung
let v = 3.14;
let t1 = 6;
let r1 = 5;
let r2 = 5;

const Volume_Tabung = v * t1 * r1 * r2;
console.log("Volume Tabung", Volume_Tabung);

// Volume Kerucut
let phi = 1 / 3;
let phi2 = 3.14;
let r3 = 5;
let r4 = 5;
let t2 = 4;

const Volume_Kerucut = phi * phi2 * r3 * r4 * t2;
console.log("Volume Kerucut", Volume_Kerucut);

// Volume Bola
let y = 4 / 3;
let phi3 = 3.14;
let r = 4 ** 3;

const Volume_Bola = y * phi3 * r;
console.log("Volume Bola: " + Volume_Bola);